package accesstypes;
public class AccessTypes{
    private int a=10;
    public int b = 20;
    protected int c = 30;
    int d = 40;

    public void display(){
        System.out.println("a: "+a);
        System.out.println("b: "+b);
        System.out.println("c: "+c);
        System.out.println("d: "+d);
    }
}

